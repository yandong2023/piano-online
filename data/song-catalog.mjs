export const songList = Object.entries(songs).map(([id, song]) => ({ id, ...song }));
export const featuredSongs = songList.filter((song) => song.featured && song.seoEnabled);
export function getSongById(id) { return songs[id] || null; }
export function getSongBySlug(slug) { return songList.find((song) => song.slug === slug) || null; }
export function getSongTitle(song, locale = 'zh') { return song?.title?.[locale] || song?.title?.zh || ''; }
export function calculateScore(correctNotes, wrongNotes) {
  if (correctNotes === 0 && wrongNotes === 0) return 0;
  const totalNotes = correctNotes + wrongNotes;
  let score = Math.round((correctNotes / totalNotes) * 100);
  if (wrongNotes > totalNotes * 0.5) score -= 20;
  return Math.max(0, Math.min(100, score));
}
